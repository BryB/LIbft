#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		r;
	int		c;

	r = 0;
	c = 1;
	i = bb_skipspecial(str);
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			c = -1;
		if (str[i] == '+')
			c = 1;
		i++;
	}
	while (str[i] && (str[i] >= '0' && str[i] <= '9'))
		r = r * 10 + (str[i++] - '0');
	return (r * c);
}
