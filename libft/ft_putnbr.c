#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < 10 && n >= 0)
	{
		i = n + '0';
		ft_putchar(i);
	}
}
