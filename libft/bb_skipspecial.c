#include "libft.h"

int		bb_skipspecial(const char *str)
{
	int	o;

	o = 0;
	while (str[o] == '\n' || str[o] == '\t' || str[o] == '\v' ||
			str[o] == ' ' || str[o] == '\f' || str[o] == '\r')
		o++;
	return (o);
}
